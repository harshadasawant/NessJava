
//byte->short->int->long
//float-> double
public class TypeCasting {
    public static void main(String[] args) {
        byte b = 8;
        short s = b;   //implicit casting
        byte v = (byte) s;  // explicit casting
        float f =7.8f;
        double d = f;
        float t = (float) d;



    }

}
